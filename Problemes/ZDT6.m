function f = ZDT6(x)

f1 = 1 - exp(-4*x(1,1)) * sin(6*pi*x(1,1)).^6;

somme = 0;
for i = 2:size(x,1)
    somme = somme + x(i);
end
g = 1 + 9 * (somme/(size(x,1)-1)).^0.25;
f2 = g * ( 1 - (f1/g).^2 );

f = [f1; f2];
